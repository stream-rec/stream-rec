package github.hua0512.utils

import java.security.cert.X509Certificate
import javax.net.ssl.SSLContext
import javax.net.ssl.SSLSocketFactory
import javax.net.ssl.TrustManager
import javax.net.ssl.X509TrustManager

/**
 * Insecure trust manager for disabling SSL verification
 * @author hua0512
 * @date : 2025/12/02 21:35
 */
object InsecureTrustManager {

  val trustAllCertificates: X509TrustManager = object : X509TrustManager {
    override fun checkClientTrusted(chain: Array<out X509Certificate>?, authType: String?) {}
    override fun checkServerTrusted(chain: Array<out X509Certificate>?, authType: String?) {}
    override fun getAcceptedIssuers(): Array<X509Certificate> = arrayOf()
  }

  val trustAllHostnameVerifier = { _: String, _: javax.net.ssl.SSLSession -> true }

  fun createSSLSocketFactory(): SSLSocketFactory {
    val sslContext = SSLContext.getInstance("TLS")
    sslContext.init(null, arrayOf<TrustManager>(trustAllCertificates), java.security.SecureRandom())
    return sslContext.socketFactory
  }
}
